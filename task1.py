# Arithmetic operations
a = 10
b = 5

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Modulus:", a % b)
print("Exponentiation:", a ** b)

# Assignment operations
c = 10
print("Initial value:", c)
c += 5
print("After += 5:", c)
c -= 3
print("After -= 3:", c)
c *= 2
print("After *= 2:", c)
c /= 4
print("After /= 4:", c)
c //= 2
print("After //= 2:", c)
c %= 3
print("After %= 3:", c)
c **= 2
print("After **= 2:", c)

# Logical operations
x = True
y = False

print("x and y:", x and y)
print("x or y:", x or y)
print("not x:", not x)

# Membership operations
my_list = [1, 2, 3, 4, 5]

print("3 in my_list:", 3 in my_list)
print("6 not in my_list:", 6 not in my_list)

# Identity operations
d = 10
e = 10
f = [1, 2, 3]
g = [1, 2, 3]

print("d is e:", d is e)
print("f is g:", f is g)
print("f is not g:", f is not g)
